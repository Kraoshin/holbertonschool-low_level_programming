#include "calc.h"

/**
 * op_add - add numbers
 * @a: first num
 * @b: second num
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract numbers
 * @a: first num
 * @b: second num
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply numbers
 * @a: first num
 * @b: second num
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div numbers
 * @a: first num
 * @b: second num
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod numbers
 * @a: first num
 * @b: second num
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
